
const Collapse = Reactstrap.Collapse;
const Navbar = Reactstrap.Navbar;
const NavbarToggler = Reactstrap.NavbarToggler;
const NavbarBrand = Reactstrap.NavbarBrand;
const Nav = Reactstrap.Nav;
const NavItem = Reactstrap. NavItem;
const NavLink = Reactstrap.NavLink;
const UncontrolledDropdown = Reactstrap.UncontrolledDropdown;
const DropdownToggle = Reactstrap.DropdownToggle;
const DropdownMenu = Reactstrap.DropdownMenu;
const DropdownItem = Reactstrap.DropdownItem;

class Menu extends React.Component {
  constructor(props) {
    super(props);

    this.toggle = this.toggle.bind(this);
    this.state = {
      isOpen: false
    };
  }
  toggle() {
    this.setState({
      isOpen: !this.state.isOpen
    });
  }
  render() {
    return (
      <div>
        <Navbar color="light" light expand="md">
          <NavbarBrand href="/">reactstrap</NavbarBrand>
          <NavbarToggler onClick={this.toggle} />
          <Collapse isOpen={this.state.isOpen} navbar>
            <Nav className="ml-auto" navbar>
              <NavItem>
                <NavLink href="/components/">Components</NavLink>
              </NavItem>
              <NavItem>
                <NavLink href="https://github.com/reactstrap/reactstrap">GitHub</NavLink>
              </NavItem>
              <UncontrolledDropdown nav inNavbar>
                <DropdownToggle nav caret>
                  Options
                </DropdownToggle>
                <DropdownMenu right>
                  <DropdownItem>
                    Option 1
                  </DropdownItem>
                  <DropdownItem>
                    Option 2
                  </DropdownItem>
                  <DropdownItem divider />
                  <DropdownItem>
                    Reset
                  </DropdownItem>
                </DropdownMenu>
              </UncontrolledDropdown>
            </Nav>
          </Collapse>
        </Navbar>
      </div>
    );
  }
}